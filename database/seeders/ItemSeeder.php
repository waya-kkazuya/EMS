    private function randomDateTime($startDate, $endDate)
    {
        $randomTimestamp = rand($startDate->timestamp, $endDate->timestamp);
        $randomDateTime = Carbon::createFromTimestamp($randomTimestamp);

        // 時間を8:00～18:00の範囲に設定
        $randomHour = rand(8, 18);
        $randomDateTime->setTime($randomHour, rand(0, 59), rand(0, 59));

        return $randomDateTime;
    }

